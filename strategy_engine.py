class Phase(str, Enum):
    WARMUP  = "warmup"
    EARLY   = "early"    # turns 1–6:   breadth priority, foundational/basic only
    MID     = "mid"      # turns 7–14:  balanced, intermediate/advanced unlocked
    LATE    = "late"     # turns 15–18: exploitation, weakness confirmation
    CLOSING = "closing"  # turns 19–20: wrap up, no new topics

class QuestionType(str, Enum):
    DEFINITION      = "definition"
    SCENARIO        = "scenario"
    WHY_PROBE       = "why_probe"
    NUMERICAL       = "numerical"
    CONTRADICTION   = "contradiction"
    PERSONAL_ANCHOR = "personal_anchor"
    RECOVERY_PROBE  = "recovery_probe"
    PRACTICAL_EXAMPLE = "practical_example"
    WARMUP          = "warmup"
    GREETING        = "greeting"

class Difficulty(str, Enum):
    FOUNDATIONAL = "foundational"   # level index 0
    BASIC        = "basic"          # level index 1
    INTERMEDIATE = "intermediate"   # level index 2
    ADVANCED     = "advanced"       # level index 3
    EXPERT       = "expert"         # level index 4

class Confidence(str, Enum):
    UNTESTED          = "untested"
    INTRODUCED        = "introduced"   # 1 turn at any depth
    CONFIRMED         = "confirmed"    # 2+ turns, reached required depth
    EXHAUSTED         = "exhausted"    # fully covered, do not revisit

class WeakFlag(str, Enum):
    NONE              = "none"
    SUSPECTED         = "suspected"    # 1 weak signal — needs confirmation
    CONFIRMED_WEAK    = "confirmed_weak"
    CONFIRMED_STRONG  = "confirmed_strong"

class CandidateLevel(str, Enum):
    FRESH_GRADUATE    = "fresh_graduate"      # level index 0
    TRAINED_FRESHER   = "trained_fresher"     # level index 1
    EXPERIENCED_JUNIOR = "experienced_junior" # level index 2
    EXPERIENCED_SENIOR = "experienced_senior" # level index 3


