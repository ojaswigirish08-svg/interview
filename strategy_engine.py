"""
strategy_engine.py
──────────────────
The interview strategy engine. Pure Python — zero LLM calls, zero I/O.
Decides WHAT to ask next, on WHICH topic, at WHAT depth, and WHY.

Architecture position:
    Session State → StrategyEngine.decide() → QuestionIntent → LLM (phrasing only)

The LLM receives a QuestionIntent and writes natural language for it.
The LLM never decides topic, type, difficulty, or concept node.

To plug in other components:
    - QuestionGenerator receives QuestionIntent and returns question text
    - EvaluationEngine calls update_after_evaluation() after each answer
    - RepetitionGuard calls get_asked_concept_nodes() for its Layer 3 check

To tune interview style:
    - Adjust PHASE_WEIGHTS for each phase
    - Adjust TURN_BUDGET_PER_LEVEL for session length
    - Adjust DEPTH_REQUIREMENT per candidate level
    - Adjust CONCEPT_TAXONOMY to add/remove concept nodes
"""

