def get_or_create_engine(session: dict) -> StrategyEngine:
    """
    Returns the StrategyEngine for a session, creating it if needed.
    Call this instead of constructing StrategyEngine directly.
    """
    engine = StrategyEngine(session)
    if "strategy_coverage" not in session:
        engine.initialize_coverage_map()
    return engine


def strategy_decide(session: dict) -> StrategyDecision:
    """
    Single entry point for the rest of the codebase.
    Returns a StrategyDecision every technical turn.

    Replaces: decide_question_type(session)
    """
    engine = get_or_create_engine(session)
    return engine.decide()


def strategy_update(session: dict, eval_result: dict,
                    topic: str, concept_node_id: str) -> None:
    """
    Single entry point for post-evaluation updates.
    Call this after every evaluation completes.

    Replaces: the scattered session state updates in submit_answer()
    """
    engine = get_or_create_engine(session)
    engine.update_after_evaluation(eval_result, topic, concept_node_id)


def strategy_coverage_for_report(session: dict) -> dict:
    """
    Returns structured topic coverage for report generation.
    Replaces: topic_map computation in generate_report()
    """
    engine = get_or_create_engine(session)
    return engine.get_coverage_summary()